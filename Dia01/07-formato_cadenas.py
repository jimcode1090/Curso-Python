# Formato de cadenas en python

var_hola = 'Hola'
var_mundo = 'Mundo'

# Imprimir los valores de las variables
print(var_hola, var_mundo)

# Concatenar las variables
print(var_hola + ' ' + var_mundo)

# Interpolación de variables
print(f'Mi cadena: {var_hola} {var_mundo}')

# Interpolación de cadenas con multi lineas
print(f'''Mi cadena: 
    {var_hola}
    {var_mundo}
''')