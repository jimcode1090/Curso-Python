# Métodos de cadenas en Python

mensaje = "Hola mundo"

# Convertir a mayúsculas
print(mensaje.upper())

# Convertir a minúsculas
print(mensaje.lower())

# Obtener la longitud de la cadena
print(len(mensaje))

# Reemplazar palabras
print(mensaje.replace("Hola", "Hello"))

# Dividir la cadena
print(mensaje.split())
