print('*** OPERADORES DE ASIGNACIÓN ***')
a = 5
print(f'Valor de a: {a}')

b = 'Saludos desde python'
print(f'Valor de b: {b}')

print('*** ASIGNACIÓN DE VALORES MULTIPLES ***')
a, b, c = 10, 'Adios desde python', 14.5
print(f'Valor de a: {a}, b: {b}, c: {c}')

print('*** ASIGNACIÓN DE UN MISMO VALOR A DISTINTAS VARIABLES ***')
x = y = z = 10
print(f'Valor de x: {x}, y: {y}, z: {z}')